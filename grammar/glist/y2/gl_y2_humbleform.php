<table width="90%" border="1" align="center">
 
  <tr>
    <td><p>Deciding when to use 謙譲語: </p>
        <ul>
          <li>use 謙譲語 to express your (or your in-group member's) lower statues.
          </li>
          <li>use 謙譲語 to  express respect toward others(superior).</li>
          <li>use 謙譲語 when your action affects others (superior) .</li>
          <li>exception:  謙譲語 are used to simply making the expression polite. </li>
          
          <li>the degree of familiarity and the genders of the persons involved
            may change the above decisions<br>
          </li>
      </ul></td>
  </tr>
</table>
